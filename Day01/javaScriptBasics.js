//Primitive datatypes

//String type
var firstName ="Swathi"
console.log(typeof(firstName))

//String type
var companyName = "abc"
console.log(typeof(companyName))

//Number type
var mobileNumber = 123456789
console.log(typeof(mobileNumber))

//boolean type
var isAutomation = true
console.log(typeof(isAutomation))

//Undefined type (not assigned)
var hasPlaywright
console.log(typeof(hasPlaywright))

//Print the values
console.log("The first name is :" +firstName)
console.log("The company name is :" +companyName)
console.log("The mobile Number is :" +mobileNumber)
